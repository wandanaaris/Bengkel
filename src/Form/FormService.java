    private void BsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsaveActionPerformed
        if (Tnofaktur.getText().isEmpty() || Tidcustomer.getText().isEmpty() || Tbiaya.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lengkapi Data!");
        } else {          
         try {
              st = cn.createStatement();
              st.executeUpdate("INSERT INTO service VALUES('"+
                      Tnofaktur.getText()+"','"+
                      Tidcustomer.getText()+"','"+
                      Tnoplat.getText()+"','"+
                      new SimpleDateFormat("yyyy-MM-dd").format(DTtgl.getDate())+"','"+
                      Tbiaya.getText()+"')");
                JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
                tampilData();
                reset();
          } catch (Exception e) {
              System.out.println("Tambah data detail service gagal : " + e);
          }
      }
    }//GEN-LAST:event_BsaveActionPerformed

